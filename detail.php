            <div class="section">
                <h2 class="section-title">Ingredients</h2>
                <ul class="ingredients-list">
                    <li>8 rice paper wrappers (22cm diameter)</li>
                    <li>200g cooked shrimp, peeled and halved lengthwise</li>
                    <li>100g rice vermicelli noodles, cooked and drained</li>
                    <li>1 large carrot, julienned</li>
                    <li>1 cucumber, julienned</li>
                    <li>2 cups fresh lettuce leaves, torn</li>
                    <li>1 cup fresh mint leaves</li>
                    <li>1 cup fresh cilantro leaves</li>
                    <li>1 cup fresh Thai basil leaves</li>
                    <li>4 tbsp crushed roasted peanuts</li>
                </ul>

                <h3 class="section-title" style="margin-top: 40px; font-size: 1.5em;">For Peanut Dipping Sauce:</h3>
                <ul class="ingredients-list">
                    <li>3 tbsp hoisin sauce</li>
                    <li>2 tbsp peanut butter</li>
                    <li>1 tbsp soy sauce</li>
                    <li>2 tsp lime juice</li>
                    <li>1 tsp sriracha sauce (optional)</li>
                    <li>2-3 tbsp warm water</li>
                    <li>Crushed peanuts for garnish</li>
                </ul>
            </div>

            <div class="section">
                <h2 class="section-title">Preparation Steps</h2>
                <ol class="steps-list">
                    <li>Prepare all your ingredients by washing and cutting the vegetables into thin strips. Cook the shrimp until pink and halved them lengthwise. Cook the rice vermicelli according to package instructions, drain, and rinse with cold water.</li>
                    <li>Fill a large shallow dish with warm water. Dip one rice paper wrapper into the water for about 5-10 seconds until it becomes soft and pliable. Remove and place on a clean, damp kitchen towel or cutting board.</li>
                    <li>On the bottom third of the rice paper, place a small amount of lettuce, then add a portion of noodles, carrot, cucumber, and herbs. Leave about 2 inches on each side.</li>
                    <li>Fold the bottom of the rice paper up over the filling. Fold in both sides tightly, like a burrito.</li>
                    <li>Place 2-3 shrimp halves along the crease, with the pink side facing down so they'll be visible through the wrapper.</li>
                    <li>Continue rolling tightly to seal. The rice paper will stick to itself. Place the finished roll on a plate and cover with a damp towel to prevent drying.</li>
                    <li>Repeat with remaining ingredients. Arrange rolls on a serving platter.</li>
                    <li>To make the sauce, whisk together hoisin sauce, peanut butter, soy sauce, lime juice, and sriracha in a small bowl. Add warm water gradually until you reach desired consistency. Sprinkle with crushed peanuts.</li>
                    <li>Serve the spring rolls immediately with the peanut dipping sauce on the side. Enjoy fresh for the best taste and texture!</li>
                </ol>
            </div>

            <div class="section">
                <div class="notes-section">
                    <h3>Chef's Notes & Tips</h3>
                    <ul class="notes-list">
                        <li>Don't soak the rice paper too long or it will tear easily. It should still feel slightly firm when you remove it from the water as it will continue to soften.</li>
                        <li>Work with one rice paper at a time and keep the others dry until you're ready to use them.</li>
                        <li>If the rice paper tears, simply wrap another sheet around it to hold everything together.</li>
                        <li>These spring rolls are best eaten within 2-3 hours of making. Store them covered with a damp towel in the refrigerator to prevent drying.</li>
                        <li>You can substitute shrimp with cooked chicken, tofu, or keep it completely vegetarian for a lighter option.</li>
                        <li>For extra crunch, add bean sprouts or thinly sliced bell peppers to your filling.</li>
                        <li>The key to perfect spring rolls is not overfilling them. Less is more when it comes to the filling amount.</li>
                        <li>If peanut butter isn't available, you can make the sauce with just hoisin and add sesame oil for richness.</li>
                    </ul>
